            <b>Resolução n° 1094, de 31 de outubro de 2017.</b> Dispõe sobre a adoção do Livro de Ordem de obras e
            serviços das profissões abrangidas pelo Sistema Confea/Crea . O CONSELHO FEDERAL DE ENGENHARIA E
            AGRONOMIA - CONFEA, no uso das atribuições que lhe confere o art. 27, alínea “f”, da Lei nº 5.194, de 24 de
            dezembro de 1966, e Considerando que compete aos Conselhos Regionais de Engenharia e Agronomia – Creas,
            com amparo na alínea “f” do art. 34 da referida Lei n° 5.194, de 1966, organizar os procedimentos de
            fiscalização das atividades desenvolvidas pelos profissionais pertencentes ao Sistema Confea/Crea;
            Considerando a necessidade de adoção de mecanismos que propiciem eficiente acompanhamento e controle da
            participação efetiva dos profissionais nas obras e serviços pelos quais são responsáveis técnicos, de sorte a
            preservar os interesses da sociedade; Considerando que os instrumentos tradicionais de fiscalização verificam a
            autoria dos projetos e a existência de responsável técnico pelas obras e serviços, mas não conseguem verificar
            o efetivo acompanhamento do profissional, RESOLVE: Art. 1º Fica instituído o Livro de Ordem de obras e
            serviços das profissões abrangidas pelo Sistema Confea/Crea. § 1° O Livro de Ordem será preferencialmente
            eletrônico e estará vinculado à respectiva Anotação de Responsabilidade Técnica - ART. § 2° O Livro de Ordem
            será obrigatório para a emissão de Certidão de Acervo Técnico – CAT aos responsáveis pela execução e
            fiscalização de obras iniciadas a partir de 1o de janeiro de 2018. § 3º Os Plenários dos Creas, a partir de
            propostas das Câmaras Especializadas, poderão definir outras atividades e serviços técnicos para os quais a
            adoção do Livro de Ordem será obrigatória para a emissão da CAT. Art. 2º O Livro de Ordem constituirá a
            memória escrita de todas as atividades relacionadas com a obra ou serviço e servirá de subsídio para: I –
            comprovar autoria de trabalhos; II – garantir o cumprimento das instruções, tanto técnicas como
            administrativas; III – dirimir dúvidas sobre a orientação técnica relativa à obra; IV – avaliar motivos de
            eventuais falhas técnicas, gastos imprevistos e acidentes de trabalho; e V – eventual fonte de dados para
            trabalhos estatísticos. Art. 3º O Livro de Ordem tem ainda por objetivo confirmar, juntamente com a ART, a
            efetiva participação do profissional na execução dos trabalhos da obra ou serviço, de modo a permitir a
            verificação da medida dessa participação, inclusive para a expedição de CAT. Art. 4° O Livro de Ordem deverá
            conter o registro, a cargo do responsável técnico, de todas as ocorrências relevantes do empreendimento. § 1°
            Serão registradas no Livro de Ordem informações tais como: I – dados do empreendimento, de seu
            proprietário, do responsável técnico e da respectiva ART; II – as datas de início e de previsão da conclusão da
            obra ou serviço; III – as datas de início e de conclusão de cada etapa programada; IV – os relatos de visitas do
            responsável técnico; V – o atual estágio de desenvolvimento do empreendimento no dia de cada visita técnica;
            VI – orientação de execução, mediante a determinação de providências relevantes para o cumprimento dos
            projetos e especificações; VII – acidentes e danos materiais ocorridos durante os trabalhos; VIII – nomes de
            empresas e prestadores de serviço contratados ou subcontratados, caracterizando seus encargos e as
            atividades, com as datas de início e conclusão, e números das ARTs respectivas; IX – os períodos de
            interrupção dos trabalhos e seus motivos, quer de caráter financeiro ou meteorológico, quer por falhas em
            serviços de terceiros não sujeitas à ingerência do responsável técnico; e X – outros fatos e observações que, a
            juízo ou conveniência do responsável técnico pelo empreendimento, devam ser registrados. § 2° A data de
            encerramento do Livro de Ordem será a mesma de solicitação da baixa por conclusão do empreendimento, por
            distrato ou por outro motivo cabível. § 3º Uma mesma obra ou empreendimento poderá contar com tantos
            Livros de Ordem quantos forem os responsáveis técnicos cujas atividades técnicas tenham obrigatoriedade de
            registro para emissão de CAT, conforme definido pelas Câmaras Especializadas. Art. 5o Os modelos porventura
            já existentes, físicos ou eletrônicos, tais como Boletim Diário, Livro de Ocorrências Diárias, Diário de Obras,
            Cadernetas de Obras etc., ainda em uso pelas empresas privadas, órgãos públicos ou autônomos, poderão ser
            admitidos como Livro de Ordem, desde que atendam às exigências desta resolução. Art. 6o Os casos omissos
            serão examinados pelas Câmaras Especializadas envolvidas com o assunto e dirimidos pelo Plenário do
            Conselho Regional. Art. 7o Esta resolução entra em vigor na data de sua publicação, com obrigatoriedade de
            implementação em todos os Creas a partir de 1o de janeiro de 2018. Art. 8o Revoga-se a Resolução no 1.024,
            de 21 de agosto de 2009.<br>
            Brasília, 31 de outubro de 2017.<br>
